console.log(`----Number 5 to 15 by incrementing by 1----`);
let i = 5, n = 15;

// while loop from i = 1 to 5
while (i <= n) {
    console.log(i);
    i += 1;
}

console.log(`----Number 50 to 40 by decrementing by 1----`);
let j = 50, num = 40;
while (j >=num) {
    console.log(j);
    j-= 1;
}

